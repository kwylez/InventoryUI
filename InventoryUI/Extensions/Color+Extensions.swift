import Foundation
import UIKit
import SwiftUI

extension UIColor {
    
    static let dashboardStart: UIColor = UIColor(named: "DashboardStart")!
    static let dashboardMiddle: UIColor = UIColor(named: "DashboardMiddle")!
    static let dashboardEnd: UIColor = UIColor(named: "DashboardEnd")!
    static let dashboardPink: UIColor = UIColor(named: "DashboardPink")!
    static let dashboardOrange: UIColor = UIColor(named: "DashboardOrange")!
    static let dashboardYellow: UIColor = UIColor(named: "DashboardYellow")!
}

extension Color {
    
    static let dashboardStart: Color = Color(.dashboardStart)
    static let dashboardMiddle: Color = Color(.dashboardMiddle)
    static let dashboardEnd: Color = Color(.dashboardEnd)
    static let dashboardPink: Color = Color(.dashboardPink)
    static let dashboardOrange: Color = Color(.dashboardOrange)
    static let dashboardYellow: Color = Color(.dashboardYellow)
}
